package com.codingnomads.part_01;

/**
 * Part 1 Exercise 2:
 * Write the necessary code to display "Hello World!" 5 times
 */

public class Exercise_02 {

    public static void main(String[] args) {

        int i = 0;

        while (i < 5) {
            System.out.println("Hello World");
            i++;
        }

    }

}