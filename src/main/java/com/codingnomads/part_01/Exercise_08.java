package com.codingnomads.part_01;

/**
 * Part 1 Exercise 8:
 * <p>
 * Write the necessary code that prints out the area
 * and perimeter of a circle with a radius of 3.14
 */

public class Exercise_08 {

    public static void main(String[] args) {

        double radius = 3.14;

        System.out.println("The area of this circle is: " + Math.PI * radius);
        System.out.println("The perimeter of this circle is: " + 2 * Math.PI * radius);


    }

}